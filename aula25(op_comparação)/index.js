/*
Operadores de comparação
>   maior
>=  maior ou igual
<   menor
<=  menor ou igual
==  igualdade (valor) 
=== igualdade estrita (valor e tipo) <--optar sempre por ele
!=  diferente (valor)
!== diferente estrito (valor e tipo) <--optar sempre por ele
*/

const comp = 10 == '10'
console.log(comp)

const comp1 = 10 === '10'
console.log(comp1)

const comp2 = 10 != '10'
console.log(comp2)

const comp3 = 10 !== '10'
console.log(comp3)